<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class QodratQuiz extends Model
{
    protected $fillable = [
        'course_id',
        'question',
        'option_a',
        'option_b',
        'option_c',
        'option_d',
        'correct_answer',
    ];

    public function course()
    {
        return $this->belongsTo(QodratCourse::class, 'course_id');
    }
}